export function useTranslate(lang: LanguageCode) {
    const t = (path: string) => {
        const keys = path.split('.');
        let result: any = translations[lang];
        for (const key of keys) {
            if (result && result[key]) {
                result = result[key];
            } else {
                return path;
            }
        }
        return result;
    };

    return { t };
}
